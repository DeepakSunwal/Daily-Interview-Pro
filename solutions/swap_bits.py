def swap(num):
    even = 0b10101010101010101010101010101010
    odd = 0b01010101010101010101010101010101
    return (num & even) >> 1 | (num & odd) << 1


def main():
    assert swap(0b10101010101010101010101010101010) == 0b01010101010101010101010101010101
    # print(swap(0b10101010101010101010101010101010))


if __name__ == '__main__':
    main()
